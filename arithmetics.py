num1=5
num2=6.0
num3=2.5
num4=7

#addition
print(" -- Addition --")
print(num1, "+", num2,"=",num1+num2)
print(num2, "+",num3,"=",num2+num3)
print(num1, "+",num4,"=",num1+num4)

#substraction
print(" -- Substraction --")
print(num1, "-", num2,"=",num1-num2)
print(num2, "-",num3,"=",num2-num3)
print(num4, "-",num1,"=",num4-num1)

#multiplication
print(" -- multiplication --")
print(num1, "*", num2,"=",num1*num2)
print(num2, "*",num3,"=",num2*num3)
print(num4, "*",num1,"=",num4*num1)

print(" -- Power: Returns first raised to power second,  ** --")
print(num1, "**", num2,"=",num1**num2)
print(num2, "**",num3,"=",num2**num3)
print(num4, "**",num1,"=",num4**num1)

#division
print(" -- division, out is float value  / --")
print(num1, "/", num2,"=",num1/num2)
print(num2, "/",num3,"=",num2/num3)
print(num4, "/",num1,"=",num4/num1)

print(" -- Floor division // --")
print(num1, "//", num2,"=",num1//num2)
print(num2, "//",num3,"=",num2//num3)
print(num4, "//",num1,"=",num4//num1)

print(" -- Modulus: returns the remainder,  % --")
print(num1, "%", num2,"=",num1%num2)
print(num2, "%",num3,"=",num2%num3)
print(num4, "%",num1,"=",num4%num1)

#round to two deicmal places
print("round of :",num2**num3," is ",round(num2**num3,2))